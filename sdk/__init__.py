def generate_build_info(target: PathLike):
    build_time = time.time()
    ctx = context()

    print(f"Generate build info to '{target}'.")
    target = Path(ctx.source, target)
    extension = target.suffix.removeprefix(".")
    auto_message = f"AUTO-GENERATED BY THE BUILD SYSTEM. Edit responsibly. If you need to set values for development, better call `dome dev`."
    content = ""
    if extension == "py":
        content = f"# {auto_message}\nproject_id = \"{_project.id}\"\nversion = \"{_target_version}\"\ntimestamp = {build_time}\ndebug = {'True' if _target_debug else 'False'}"
    elif extension in ["js", "ts"]:
        BRACKET_LEFT = "{"
        BRACKET_RIGHT = "}"
        content = f"// {auto_message}\nconst project_id = \"{_project.id}\";\nconst version = \"{_target_version}\";\nconst timestamp = {build_time};\nconst debug = {'true' if _target_debug else 'false'};\nexport {BRACKET_LEFT} project_id, version, timestamp, debug {BRACKET_RIGHT};\n"
    else:
        raise Exception(f"Unsupported build info extension '{extension}' at location '{target}'.")
    with target.open("w+") as f:
        f.write(content)


